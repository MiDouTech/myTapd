async function searchTickets(keyword: string): Promise<void> {
  const currentToken = ++ticketSearchToken
  ticketLoading.value = true
  try {
    const normalized = keyword.trim()
    // 使用 keyword：后端对编号与标题做 OR 模糊匹配；勿同时传 ticketNo+title（会变成 AND，几乎搜不到）
    const result = await getTicketPage({
      pageNum: 1,
      pageSize: 20,
      view: 'all',
      keyword: normalized || undefined,
    })
    if (currentToken !== ticketSearchToken) {
      return
    }
    mergeTicketOptionsBySearch(result.records || [], normalized)
  } finally {
    if (currentToken === ticketSearchToken) {
      ticketLoading.value = false
    }
  }
}

