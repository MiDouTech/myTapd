function normalizeStepsToOneLine(raw: string): string {
  const lines = raw
    .split(/\n/)
    .map((line) => line.replace(/^\s*\d+\s*[.、)）]\s*/, '').trim())
    .filter(Boolean)
  const joined = lines.join(' → ')
  return joined.length > 120 ? joined.slice(0, 117) + '…' : joined
}

function trimTrailingPunct(text: string): string {
  return text.replace(/[，。！？,.!?\s]+$/, '').trim()
}

type TicketSummarySource = {
  ticketNo?: string
  title?: string
  description?: string
  bugCustomerInfo?: { problemDesc?: string; expectedResult?: string } | null
  bugTestInfo?: { actualResult?: string; reproduceSteps?: string } | null
}

function buildProblemDescSummary(tickets: TicketSummarySource[]): string {
  const valid = tickets.filter(
    (t) => t.title || t.description || t.bugCustomerInfo?.problemDesc || t.bugTestInfo?.actualResult,
  )
  if (valid.length === 0) return ''

  const sections: string[] = []

  if (valid.length > 1) {
    const nos = valid
      .map((t) => t.ticketNo)
      .filter(Boolean)
      .join('、')
    sections.push(`本次简报共关联 ${valid.length} 个工单${nos ? `（${nos}）` : ''}，问题概述如下：`)
  }

  valid.forEach((ticket, idx) => {
    const ref = [ticket.ticketNo, ticket.title].filter(Boolean).join(' ')
    const sentences: string[] = []

    const problem = trimTrailingPunct(ticket.bugCustomerInfo?.problemDesc || ticket.description || '')
    if (problem) {
      sentences.push(problem)
    }

    const actual = trimTrailingPunct(ticket.bugTestInfo?.actualResult || '')
    const expected = trimTrailingPunct(ticket.bugCustomerInfo?.expectedResult || '')
    if (actual && expected) {
      sentences.push(`实际${actual}，与预期（${expected}）不符`)
    } else if (actual && !problem.includes(actual.slice(0, 12))) {
      sentences.push(`实际现象为${actual}`)
    }

    const rawSteps = (ticket.bugTestInfo?.reproduceSteps || '').trim()
    if (rawSteps) {
      const stepsLine = normalizeStepsToOneLine(rawSteps)
      if (stepsLine) {
        sentences.push(`复现路径：${stepsLine}`)
      }
    }

    if (sentences.length === 0) {
      if (ref) sentences.push(`相关工单：${ref}`)
      else return
    }

    let para = sentences.join('，') + '。'
    para = para.replace(/[，。！？]+([，。！？])/g, '$1')

    if (valid.length > 1) {
      sections.push(`${idx + 1}. 【${ref}】${para}`)
    } else {
      sections.push(para)
    }
  })

  return sections.join('\n').slice(0, 1000)
}

const SEVERITY_ORDER: Record<string, number> = { P0: 0, P1: 1, P2: 2, P3: 3 }

function pickHighestSeverity(levels: (string | undefined | null)[]): string {
  let best = 'P2'
  let bestRank: number = SEVERITY_ORDER['P2'] ?? 2
  for (const level of levels) {
    if (!level) continue
    const rank = SEVERITY_ORDER[level]
    if (rank !== undefined && rank < bestRank) {
      best = level
      bestRank = rank
    }
  }
  return best
}

async function autoFillFromTickets(ticketIds: number[]): Promise<void> {
  if (ticketIds.length === 0) return
  prefillLoading.value = true
  try {
    const details = await Promise.all(ticketIds.map((id) => getTicketDetail(id)))

    const newProblemDesc = buildProblemDescSummary(details)
    if (form.problemDesc === lastAutoGenerated.problemDesc) {
      form.problemDesc = newProblemDesc
      lastAutoGenerated.problemDesc = newProblemDesc
    }

    const firstTicket = details[0]
    if (firstTicket) {
      const newSeverity = pickHighestSeverity(details.map((d) => d.bugTestInfo?.severityLevel))
      if (form.severityLevel === lastAutoGenerated.severityLevel) {
        form.severityLevel = newSeverity
        lastAutoGenerated.severityLevel = newSeverity
      }

      const newImpact = details
        .map((d) => d.bugTestInfo?.impactScope || '')
        .filter(Boolean)
        .join('；')
        .slice(0, 500)
      if (newImpact && form.impactScope === lastAutoGenerated.impactScope) {
        form.impactScope = newImpact
        lastAutoGenerated.impactScope = newImpact
      }

      const newSolution = (firstTicket.bugDevInfo?.fixSolution || '').slice(0, 1000)
      if (newSolution && form.solution === lastAutoGenerated.solution) {
        form.solution = newSolution
        lastAutoGenerated.solution = newSolution
      }

      // Auto-fill reporter from the first ticket's creator, only when not manually set
      const newReporterId = firstTicket.creatorId
      if (newReporterId && form.reporterId === lastAutoGenerated.reporterId) {
        form.reporterId = newReporterId
        lastAutoGenerated.reporterId = newReporterId
      }
    }
  } finally {
    prefillLoading.value = false
  }
}

