-- ============================================================
-- V49__align_bugreport_defect_category_wording.sql
-- 对齐 Bug 简报缺陷分类 16 类文案口径（增量兜底）
-- ============================================================

UPDATE `dict_defect_category`
SET
    `description` = CASE `name`
        WHEN '页面异常' THEN '由于页面排版不合理、样式、文案错误，页面显示重叠等导致的页面性错误。'
        WHEN '交互异常' THEN '由于前端用户交互体验、接口调用导致的错误。'
        WHEN '设计问题' THEN '逻辑删除无“确认窗”、用户体验感低、注释描述有歧义或存在错别字、相关提示语缺失、光标位置不正确、文字排列不整齐，相关功能场景未考虑等。'
        WHEN '错误逻辑' THEN '由于业务逻辑、代码逻辑异常导致的错误。'
        WHEN '错误代码' THEN '逻辑正确的情况下由于编码问题导致的错误。'
        WHEN '错误数据' THEN '由于数据与业务不兼容导致的错误。'
        WHEN '错误配置' THEN '由于配置文件异常导致（参数配置错误等）的错误。'
        WHEN '错误发布' THEN '由于灰度或线上发布流程异常导致的错误。'
        WHEN '基础服务' THEN '由于基础组件、服务网关、任务调度、ES 搜索引擎等服务故障导致的错误。'
        WHEN '基础设施' THEN '数据库相关（如数据库配置、数据库定时作业运行故障、数据库脚本操作、数据库高可用性架构等）、缓存Redis 相关、Nginx 反向代理、web 负载均衡等。'
        WHEN '兼容问题' THEN '由于浏览器（不同的浏览器、或者浏览器版本太低）、移动设备或数据库版本不兼容导致的错误。'
        WHEN '架构问题' THEN '由于架构不适用导致的错误，如无法支持大数据量存储、系统扩展性差等。'
        WHEN '性能问题' THEN '并发产生的问题、页面响应时间长、服务器资源耗用过高、死锁、内存泄露、难以满足系统性能要求等。'
        WHEN '安全问题' THEN '账号越权、sql 注入漏洞、频率未做限制（比如短信发送次数，登录密码次数未做限制）等。'
        WHEN '安装部署' THEN '服务器初始化（操作系统安装，基础环境支持）、软件安装（比如数据库管理系统，ES 程序等）、编译后代码部署等产生的问题。'
        WHEN '第三方原因' THEN '由于第三方服务异常导致的错误。如：腾讯云服务器网络、七牛云存储、大禹高防及 SSL 证书等。'
        ELSE `description`
    END,
    `sort_order` = CASE `name`
        WHEN '页面异常' THEN 1
        WHEN '交互异常' THEN 2
        WHEN '设计问题' THEN 3
        WHEN '错误逻辑' THEN 4
        WHEN '错误代码' THEN 5
        WHEN '错误数据' THEN 6
        WHEN '错误配置' THEN 7
        WHEN '错误发布' THEN 8
        WHEN '基础服务' THEN 9
        WHEN '基础设施' THEN 10
        WHEN '兼容问题' THEN 11
        WHEN '架构问题' THEN 12
        WHEN '性能问题' THEN 13
        WHEN '安全问题' THEN 14
        WHEN '安装部署' THEN 15
        WHEN '第三方原因' THEN 16
        ELSE `sort_order`
    END,
    `is_active` = 1,
    `update_by` = 'system'
WHERE `deleted` = 0
  AND `name` IN (
    '页面异常', '交互异常', '设计问题', '错误逻辑', '错误代码', '错误数据', '错误配置', '错误发布',
    '基础服务', '基础设施', '兼容问题', '架构问题', '性能问题', '安全问题', '安装部署', '第三方原因'
  );
